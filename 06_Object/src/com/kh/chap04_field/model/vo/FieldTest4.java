package com.kh.chap04_field.model.vo;

// 상수필드 
public class FieldTest4 {

	/*
	 * *상수필드 (public static final)
	 * 
	 * public static final
	 * public final static --> 둘다 예약어 이기 떄문에 위치 상관없음 
	 * 
	 * static - 공유의 개념 (값 변경 가능 O )
	 * final - 상수의 개념(한번 지정된 값 변경 X)
	 * 
	 * 
	 */
	
	public static final int NUM = 1;	// 상수 인것을 알수 있겠끔 변수명은 대문자로!!!
}
